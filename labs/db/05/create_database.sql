CREATE TABLE [table_name_2] (
    id              SERIAL          PRIMARY KEY,
    name            VARCHAR(100)    NOT NULL,
    description     TEXT,
    is_active       BOOLEAN         DEFAULT TRUE
);

-- ----------------------------------------------------------------------------
-- Таблица 3: [Основная таблица с внешними ключами]
-- Описание: [Назначение таблицы]
-- ----------------------------------------------------------------------------
CREATE TABLE [table_name_3] (
    id              SERIAL          PRIMARY KEY,
    [fk_column]     INTEGER         NOT NULL,           -- Внешний ключ
    [data_column]   VARCHAR(200),
    amount          DECIMAL(10,2)   CHECK (amount > 0), -- Ограничение CHECK
    status          VARCHAR(20)     DEFAULT 'new',
    created_at      TIMESTAMP       DEFAULT NOW()
);

-- ----------------------------------------------------------------------------
-- Таблица 4: [Таблица-связка для отношения многие-ко-многим]
-- Описание: Связывает [table_A] и [table_B]
-- ----------------------------------------------------------------------------
CREATE TABLE [junction_table] (
    [table_a_id]    INTEGER         NOT NULL,
    [table_b_id]    INTEGER         NOT NULL,
    [extra_field]   VARCHAR(100),
    created_at      TIMESTAMP       DEFAULT NOW(),
    
    -- Составной первичный ключ
    PRIMARY KEY ([table_a_id], [table_b_id])
);


